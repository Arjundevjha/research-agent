APA_CITATION_RULES = """
## APA 7th Edition Citation Rules — Follow These Exactly

### In-text citations
- One author: (Smith, 2024)
- Two authors: (Smith & Jones, 2024)
- Three or more authors: (Smith et al., 2024)
- Direct quote: (Smith, 2024, p. 12)
- Organization as author: (World Health Organization, 2024)
- No author: ("Article Title," 2024)
- No date: (Smith, n.d.)

### References section
Every source cited in the text MUST appear in the References section at the end.
Format each reference like this:

For a web article:
Author, A. A. (Year, Month Day). Title of the article. *Site Name*. https://url

For a journal article:
Author, A. A., & Author, B. B. (Year). Title of the article. *Journal Name*, *Volume*(Issue), Pages. https://doi.org/xxx

For a webpage with no author:
Title of the page. (Year). *Site Name*. Retrieved Month Day, Year, from https://url

For an organization author:
Organization Name. (Year). *Title of the document*. https://url

### Formatting rules
- Hanging indent on each reference (first line flush left, subsequent lines indented)
- Alphabetical order by first author's last name
- Italicize journal/site names and volume numbers
- Include DOI or URL for every source
- Use "&" before the last author in multi-author references
"""

ANTI_JARGON_RULES = """
## Writing Style Rules — Non-Negotiable

1. NO filler phrases. Never write:
   - "In the rapidly evolving landscape of..."
   - "It is important to note that..."
   - "As we navigate the complexities of..."
   - "In today's fast-paced world..."
   - "This groundbreaking/revolutionary..."
   - "Leveraging cutting-edge..."
   - "A paradigm shift..."
   - "Synergize", "optimize", "utilize" (use "use")
   - "Facilitate" (use "help" or "enable")
   - "Methodology" (use "method")

2. Write like a smart person explaining things to another smart person.
   - Be direct. Say what happened, what it means, and why it matters.
   - Use short sentences when possible.
   - Prefer concrete examples over abstract descriptions.
   - If a simpler word works, use it.

3. Every paragraph must contain at least one specific fact, number, or finding.
   No paragraphs of pure opinion or vague claims.

4. Every factual claim MUST have an APA in-text citation.
   If you cannot cite it, do not include it.
"""

